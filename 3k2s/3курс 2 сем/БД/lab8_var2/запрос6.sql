SELECT товары.НаимТовара, запасы.Количество, товары.Цена1, подразделения.НаимПодраделения
  FROM товары INNER JOIN
  запасы ON товары.КодТовара = запасы.КодТовара INNER JOIN
  подразделения ON запасы.КодПодразделения = подразделения.КодпПодразделения
  WHERE (запасы.КодПодразделения = '0429')
  ORDER BY товары.НаимТовара